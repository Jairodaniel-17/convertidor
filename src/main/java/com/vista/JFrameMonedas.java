    public void rellenar(){
        List<String> nombresMonedas = nombres_de_las_monedas.obtenerNombresMonedas();
        Collections.sort(nombresMonedas);
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        for (String nombresMoneda : nombresMonedas) {
            jComboBox1.addItem(nombresMoneda);
            jComboBox2.addItem(nombresMoneda);
        }
    }
    public double monto() {
        return Double.parseDouble(monto.getText());
    }
    
    public String monedaOrigen(){
        return jComboBox1.getSelectedItem().toString();
    }
    public String monedaDestino(){
        return jComboBox2.getSelectedItem().toString();
    }
    
    public void resultadoConversion(){
        String resultadoDeLaConversion = conversor.resultado(monto(), monedaOrigen(), monedaDestino());
        resultado.setText(resultadoDeLaConversion);
    }

